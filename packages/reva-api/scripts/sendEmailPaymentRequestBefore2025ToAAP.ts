interface AapData {
  aapLabel: string;
  aapEmail: string;
  candidates: { fullName: string; candidacyUrl: string }[];
}

const sendEmailPaymentRequestBefore2025ToAAP = async (): Promise<void> => {
  try {
    console.log(`Processing ${candidacyIds.length} candidacies...`);

    const candidacies = await prismaClient.candidacy.findMany({
      where: {
        id: {
          in: candidacyIds,
        },
      },
      include: {
        organism: true,
        candidate: true,
      },
    });

    console.log(`Found ${candidacies.length} candidacies`);

    const aaps: Record<string, AapData> = {};

    for (const candidacy of candidacies) {
      const aapId = candidacy.organism?.id;
      const aapEmail =
        candidacy.organism?.emailContact ||
        candidacy.organism?.contactAdministrativeEmail;
      const candidateFullName =
        candidacy.candidate?.firstname && candidacy.candidate?.lastname
          ? `${candidacy.candidate.firstname} ${candidacy.candidate.lastname}`.trim()
          : null;
      const aapLabel = candidacy.organism?.label;

      if (!aapEmail || !candidateFullName || !aapLabel || !aapId) {
        console.warn(
          `Skipping candidacy ${candidacy.id}: missing required data`,
        );
        continue;
      }

      const candidate = {
        fullName: candidateFullName,
        candidacyUrl: getBackofficeUrl({
          path: `/candidacies/${candidacy.id}`,
        }),
      };

      if (!aaps[aapId]) {
        aaps[aapId] = {
          aapLabel,
          aapEmail,
          candidates: [candidate],
        };
      } else {
        aaps[aapId].candidates.push(candidate);
      }
    }

    console.log(`Grouped into ${Object.keys(aaps).length} AAPs`);

    for (const [_, data] of Object.entries(aaps)) {
      console.log("data", data);
      await sendEmailUsingTemplate({
        templateId: 646,
        to: { email: data.aapEmail },
        params: {
          aapLabel: data.aapLabel,
          candidates: data.candidates,
        },
      });
    }
  } catch (error) {
    console.error("Error in sendEmailPaymentRequestBefore2025ToAAP:", error);
    throw error;
  }
};

const main = async (): Promise<void> => {
  try {
    console.log("Starting email payment request process...");
    await sendEmailPaymentRequestBefore2025ToAAP();
    console.log("Email payment request process completed successfully");
  } catch (error) {
    console.error("Failed to send email payment requests:", error);
    process.exit(1);
  } finally {
    await prismaClient.$disconnect();
  }
};

main();
